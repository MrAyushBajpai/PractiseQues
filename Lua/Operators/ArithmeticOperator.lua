-- Arithmetic Operators

-- 1. Addition (+)
local a = 10
local b = 5
local sum = a + b
print("Addition (a + b):", sum)  -- Output: Addition (a + b): 15

-- 2. Subtraction (-)
local difference = a - b
print("Subtraction (a - b):", difference)  -- Output: Subtraction (a - b): 5

-- 3. Multiplication (*)
local product = a * b
print("Multiplication (a * b):", product)  -- Output: Multiplication (a * b): 50

-- 4. Division (/)
local quotient = a / b
print("Division (a / b):", quotient)  -- Output: Division (a / b): 2

-- 5. Modulo (%)
local remainder = a % b
print("Modulo (a % b):", remainder)  -- Output: Modulo (a % b): 0

-- 6. Exponentiation (^)
local exponentiation = a ^ b
print("Exponentiation (a ^ b):", exponentiation)  -- Output: Exponentiation (a ^ b): 100000

-- 7. Unary negation (-)
local negation = -a
print("Unary negation (-a):", negation)  -- Output: Unary negation (-a): -10

-- 8. Complex expression
local complex_result = (a + b) * (a - b) / b
print("Complex expression ((a + b) * (a - b) / b):", complex_result)  -- Output: Complex expression ((a + b) * (a - b) / b): 15
