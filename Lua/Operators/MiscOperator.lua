-- Misc Operators

-- Variables for demonstration
local a = "Hello"
local b = "World"

-- 1. String Concatenation (..)
local concatenatedString = a .. " " .. b
print("String concatenation (a .. ' ' .. b):", concatenatedString)  -- Output: String concatenation (a .. ' ' .. b): Hello World

-- 2. Length Operator (#)
local lengthOfString = #"Hello World"
print("Length of string (#'Hello World'):", lengthOfString)  -- Output: Length of string (#'Hello World'): 11