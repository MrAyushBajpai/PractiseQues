#include <iostream>
using namespace std;

int main() {
    // Addition Operator (+)
    cout << "Addition Operator (+):" << endl;
    
    // Integer addition
    int x = 5, y = 3;
    cout << "x + y = " << x + y << endl;  // Example of integer addition
    
    // Floating point addition
    double a = 2.5, b = 3.7;
    cout << "a + b = " << a + b << endl;  // Example of floating point addition
    
    // Mixed addition
    cout << "x + a = " << x + a << endl;  // Mixed integer and floating point addition
    
    return 0;
}
