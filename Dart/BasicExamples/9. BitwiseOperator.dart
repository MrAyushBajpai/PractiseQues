void main(){
    int a = 5; // Binary: 0101
    int b = 3; // Binary: 0011

    print("Bitwise AND: ${a & b}");
    print("Bitwise OR: ${a | b}");
    print("Bitwise XOR: ${a ^ b}");
    print("Bitwise NOT: ${~a}");
    print("Bitwise Left Shift: ${a << 1} ");
    print("Bitwise Right Shift: ${a >> 1}");
}