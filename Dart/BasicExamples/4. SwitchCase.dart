void main(){
    String grade = 'B';

    switch(grade){
        case 'A':
            print('Excellent!');
            break;
        case 'B':
            print('Good Job!');
            break;
        case 'C':
            print('You can do better');
            break;
        case 'D':
            print('Try Harder Next Time');
            break;
        default:
            print('Invalid Grade'); 
    }
}