#include <iostream>
using namespace std;

int main() {
    // Multiplication Operator (*)
    cout << "Multiplication Operator (*):" << endl;
    
    // Integer multiplication
    int x = 7, y = 3;
    cout << "x * y = " << x * y << endl;  // Example of integer multiplication
    
    // Floating point multiplication
    double a = 2.5, b = 4.0;
    cout << "a * b = " << a * b << endl;  // Example of floating point multiplication
    
    // Mixed multiplication
    cout << "x * b = " << x * b << endl;  // Mixed integer and floating point multiplication
    
    return 0;
}
