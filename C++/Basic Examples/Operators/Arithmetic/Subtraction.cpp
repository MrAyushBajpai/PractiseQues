#include <iostream>
using namespace std;

int main() {
    // Subtraction Operator (-)
    cout << "Subtraction Operator (-):" << endl;
    
    // Integer subtraction
    int x = 10, y = 4;
    cout << "x - y = " << x - y << endl;  // Example of integer subtraction
    
    // Floating point subtraction
    double a = 5.7, b = 2.3;
    cout << "a - b = " << a - b << endl;  // Example of floating point subtraction
    
    // Mixed subtraction
    cout << "x - b = " << x - b << endl;  // Mixed integer and floating point subtraction
    
    return 0;
}
