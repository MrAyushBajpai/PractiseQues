#include <iostream>
using namespace std;

int main() {
    // Division Operator (/)
    cout << "Division Operator (/):" << endl;
    
    // Integer division
    int x = 10, y = 3;
    cout << "x / y = " << x / y << endl;  // Example of integer division
    
    // Floating point division
    double a = 7.5, b = 2.5;
    cout << "a / b = " << a / b << endl;  // Example of floating point division
    
    // Mixed division
    cout << "x / b = " << x / b << endl;  // Mixed integer and floating point division
    
    return 0;
}
